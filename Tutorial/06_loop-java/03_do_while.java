public class do_while {
    public static void main(String[] args) {
        int i = 1;

        do {
            System.out.println(i + " Java is awesome"); // it must be pritned
            i++;
        } while (i < 1);

        int j = 1;

        do {
            System.out.println(j + " Java is awesome");
            j++;
        } while (j < 6);
    }
}