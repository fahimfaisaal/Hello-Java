public class Relational {
    public static void main(String[] args) {
        int a = 5;
        int b = 10;

        // == equality operator return (true or false)
        System.out.println(a == b);
        System.out.println(a + 5 == b);

        // != not equal operator return (true or false)
        System.out.println(a != b);
        System.out.println(a + 5 != b);

        // > greater than operator return (true or false)
        System.out.println(b > a);

        // >= greater than of equal operator return (true or false)
        System.out.println(b >= a);

        // < less than operator return (true or false)
        System.out.println(a < b);

        // <= less than or equal operaotor return (true or false)
        System.out.println(a <= b);
    }
}